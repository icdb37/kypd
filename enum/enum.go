// Package enum 枚举类型
package enum

const (
	// Compress 压缩
	Compress = 0x01
	// Encrypt 加密
	Encrypt = 0x02
	// Replace 替换
	Replace = 0x03
	// Shuffle 打乱
	Shuffle = 0x04
)

const (
	// Fixed 固定间隔
	Fixed = 0x01
	// Accum 累加间隔
	Accum = 0x02
)

const (
	// Bit4 4位
	Bit4 = 4
	// Bit8 8位
	Bit8 = 8
	// Bit16 16位
	Bit16 = 16
	// Bit32 32位
	Bit32 = 32
	// Bit48 48位
	Bit48 = 48
	// Bit64 64位
	Bit64 = 64
)

const (
	// Byte8 8 字节
	Byte8 = 8
)

const (
	// KB
	KB = 1024
	// MB
	MB = 1024 * KB
	// GB
	GB = 1024 * MB
)
